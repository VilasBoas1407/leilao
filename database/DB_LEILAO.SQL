CREATE DATABASE DB_LEILAO
USE DB_LEILAO

CREATE TABLE TB_USUARIO (
	ID_USUARIO INT NOT NULL IDENTITY(1,1) PRIMARY KEY,
	DS_USUARIO VARCHAR(100) NOT NULL,
	DS_SENHA VARCHAR(150) NOT NULL,
	FL_ATIVO BIT NOT NULL,
)

CREATE TABLE TB_LEILAO(
	ID_LEILAO INT NOT NULL IDENTITY(1,1),
	DS_NOME_LEILAO VARCHAR(100) NOT NULL,
	VL_INICIAL FLOAT NOT NULL,
	FL_PRODUTO_USUADO BIT NOT NULL,
	ID_USUARIO_RESPONSAVEL INT NOT NULL,
	DT_ABERTURA DATETIME NOT NULL,
	DT_FINALIZACAO DATETIME NOT NULL
	FOREIGN KEY (ID_USUARIO_RESPONSAVEL) REFERENCES TB_USUARIO(ID_USUARIO)
)
